
const age = 11

if (age >= 0  && age <= 1) {
    console.log('infant')
} else if (age >= 1 && age <= 3) {
    console.log('toddler')
} else if (age >= 4 && age <= 9) {
    console.log('child')
} else if (age >= 10 && age <= 12) {
    console.log('preteen')
} else if (age >= 13 && age <= 17) {
    console.log('teen')
} else if (age >= 28 && age <= 20) {
    console.log('young adult')
}